/**
 * @brief  The application entry point.
 * @retval int
 */
int main(void) {
	/* USER CODE BEGIN 1 */
	uint32_t ticks_adc;	//seran locales
	uint32_t ticks_but;
	uint16_t ry, rx;
	uint8_t r3;
	uint8_t s_r3 = 0;
	int8_t q_deb = 0;
	/* USER CODE END 1 */

	/* MCU Configuration--------------------------------------------------------*/

	/* Reset of all peripherals, Initializes the Flash interface and the Systick. */
	HAL_Init();

	/* USER CODE BEGIN Init */

	/* USER CODE END Init */

	/* Configure the system clock */
	SystemClock_Config();

	/* USER CODE BEGIN SysInit */

	/* USER CODE END SysInit */

	/* Initialize all configured peripherals */
	MX_GPIO_Init();
	MX_USB_DEVICE_Init();
	MX_ADC1_Init();
	/* USER CODE BEGIN 2 */
	ticks_adc = HAL_GetTick();
	ticks_but = HAL_GetTick();
	HAL_ADCEx_Calibration_Start(&hadc1);
	/* USER CODE END 2 */

	/* Infinite loop */
	/* USER CODE BEGIN WHILE */
	while (1) {
		/* USER CODE END WHILE */

		/* USER CODE BEGIN 3 */
		if ((HAL_GetTick() - ticks_adc) >= TICKS_ADC_MS) {
			ticks_adc = HAL_GetTick();
			HAL_ADC_Start(&hadc1);
			HAL_ADC_PollForConversion(&hadc1, 10);
			demora_software(DELAY_ADC);
			rx = HAL_ADC_GetValue(&hadc1);
			HAL_ADC_PollForConversion(&hadc1, 10);
			demora_software(DELAY_ADC);
			ry = HAL_ADC_GetValue(&hadc1);
			HAL_ADC_Stop(&hadc1);
			HAL_GPIO_TogglePin(GPIOC, GPIO_PIN_13);

		}
		if ((HAL_GetTick() - ticks_but) >= TICKS_BUT_MS) {
			ticks_but = HAL_GetTick();
			switch (s_r3) {
			case 0:
				if (HAL_GPIO_ReadPin(GPIOA, GPIO_PIN_2))
					s_r3 = 1;
				break;

			case 1:
				if (HAL_GPIO_ReadPin(GPIOA, GPIO_PIN_2))
					q_deb++;
				else
					q_deb--;

				if (q_deb == 10) {
					q_deb = 0;
					s_r3 = 2;
					r3 ^= 0x01;
				}
				if (q_deb == -10) {
					q_deb = 0;
					s_r3 = 0;
					r3 ^= 0x01;
				}
				break;

			case 2:
				if (!HAL_GPIO_ReadPin(GPIOA, GPIO_PIN_2)) {
					q_deb--;
				}
				if (q_deb == -10) {
					q_deb = 0;
					s_r3 = 0;
				}

				break;
			default:
				s_r3 = 0;
				r3 = 0;
				q_deb = 0;
				break;

			}

		}

		//UNUSED(dato_ch0);
		//UNUSED(dato_ch1);
	}
	/* USER CODE END 3 */
}

